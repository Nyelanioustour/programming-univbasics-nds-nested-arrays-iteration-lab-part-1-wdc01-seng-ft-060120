def find_even_values(src)
  count = 0 
  output = []
  output_count = 0
  
  while count < src.length do
    if src[count] % 2 == 1
    output << src[count]
  end 
end
  count += 1
  # Given an Array of Arrays of Integers ( [ [10, 11], [99, 50, 3, 4], [23, 41] ] ):
  #
  # Output all even values in each nested array
  
  while output.length < output_count do
  
    output[count]
  end
end